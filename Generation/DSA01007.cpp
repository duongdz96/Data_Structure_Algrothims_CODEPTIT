void sinh()
{
    int i = n;
    while (i >= 1 && a[i] == 1)
    {
        a[i] = 0;
        i--;
    }
    if (i == 0)
        ok = 0;
    else
        a[i] = 1;
}
void solve()
{
    cin >> n;
    ok = 1;
    ktao();
    while (ok)
    {
        for (int i = 1; i <= n; i++)
        {
            if (a[i] == 1)
                cout << "B";
            else
                cout << "A";
        }
        cout << " ";
        sinh();
    }
}
